<script>
export default {
  data() {
    return {};
  },
  mounted() {
    this.jsForViewtab($(".cs_product_tab"));
    this.collapse();
  },
  methods: {
    jsForViewtab($obj) {
      $obj
        .children(".cs_product_tab_head")
        .children("ul")
        .children("li")
        .click(function () {
          var _index = $(this).index();
          $(this).addClass("selected").siblings().removeClass("selected");
          $(this)
            .parents(".cs_product_tab_head")
            .siblings(".cs_product_tab_tbody")
            .children()
            .eq(_index)
            .show()
            .siblings()
            .hide();
        });
    },
    collapse(){
	$('.fb_title').click(function(){
		var _content = $(this).siblings('#infor_content');
		if (_content.is(':visible')) {
			_content.hide();
            $(this).children('i').removeClass('icon-slidedown').addClass('icon-slideup');
            $(this).addClass("addBottomLine");
		} else{
			_content.show();
            $(this).children('i').removeClass('icon-slideup').addClass('icon-slidedown');
            $(this).removeClass("addBottomLine");
		}
	});

}

  },
};
</script>

